
package modelos;

public class Escolaridade
{
    private String serie;
    private String nomeEscola;
    private String municipioEscola;
    private String dependenciasAdministrativa;
    private String anoLetivo;
    private String eja;

    public String getSerie() {
        return serie;
    }

    public void setSerie(String serie) {
        this.serie = serie;
    }

    public String getNomeEscola() {
        return nomeEscola;
    }

    public void setNomeEscola(String nomeEscola) {
        this.nomeEscola = nomeEscola;
    }

    public String getMunicipioEscola() {
        return municipioEscola;
    }

    public void setMunicipioEscola(String municipioEscola) {
        this.municipioEscola = municipioEscola;
    }

    public String getDependenciasAdministrativa() {
        return dependenciasAdministrativa;
    }

    public void setDependenciasAdministrativa(String dependenciasAdministrativa) {
        this.dependenciasAdministrativa = dependenciasAdministrativa;
    }

    public String getAnoLetivo() {
        return anoLetivo;
    }

    public void setAnoLetivo(String anoLetivo) {
        this.anoLetivo = anoLetivo;
    }

    public String getEja() {
        return eja;
    }

    public void setEja(String eja) {
        this.eja = eja;
    }
    
}
