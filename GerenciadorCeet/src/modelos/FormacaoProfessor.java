
package modelos;


public class FormacaoProfessor
{
    private int  codFormacao ;
    private String tipo;
    private String curso;
    private String instituicao;
    private String situacao;
    private String anoConclusao;
    private String anoIncio;

    /**
     * @return the codFormacao
     */
    public int getCodFormacao() {
        return codFormacao;
    }

    /**
     * @param codFormacao the codFormacao to set
     */
    public void setCodFormacao(int codFormacao) {
        this.codFormacao = codFormacao;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the curso
     */
    public String getCurso() {
        return curso;
    }

    /**
     * @param curso the curso to set
     */
    public void setCurso(String curso) {
        this.curso = curso;
    }

    /**
     * @return the instituicao
     */
    public String getInstituicao() {
        return instituicao;
    }

    /**
     * @param instituicao the instituicao to set
     */
    public void setInstituicao(String instituicao) {
        this.instituicao = instituicao;
    }

    /**
     * @return the situacao
     */
    public String getSituacao() {
        return situacao;
    }

    /**
     * @param situacao the situacao to set
     */
    public void setSituacao(String situacao) {
        this.situacao = situacao;
    }

    /**
     * @return the anoConclusao
     */
    public String getAnoConclusao() {
        return anoConclusao;
    }

    /**
     * @param anoConclusao the anoConclusao to set
     */
    public void setAnoConclusao(String anoConclusao) {
        this.anoConclusao = anoConclusao;
    }

    /**
     * @return the anoIncio
     */
    public String getAnoIncio() {
        return anoIncio;
    }

    /**
     * @param anoIncio the anoIncio to set
     */
    public void setAnoIncio(String anoIncio) {
        this.anoIncio = anoIncio;
    }

   
    
     
}
