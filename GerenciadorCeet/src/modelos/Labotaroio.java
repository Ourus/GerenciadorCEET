package modelos;

public class Labotaroio {

    private int codLab;
    private String nome;
    private int numero;

    public int getCodLab() {
        return codLab;
    }

    public void setCodLab(int codLab) {
        this.codLab = codLab;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

}
